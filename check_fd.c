#include "cub3d.h"

int	ft_check_cub(char *str)
{
	int	size;

	size = ft_strlen(str);
	if (str[size - 1] == 'b' && str[size - 2] == 'u'
		&&str[size - 3] == 'c' && str[size - 4] == '.')
		return (1);
	return (0);
}
